    private boolean validateInput(){
        userId=txtUser.getText();
        userId=userId.trim();
        char[] pwd=txtPassword.getPassword();
        
        if(userId.isEmpty()||pwd.length==0)
            return false;
        else{
            password=String.valueOf(pwd);
            password=password.trim();
            return true;
        }
    }
    private String getUserType(){
        if(jRadioAdmin.isSelected()){
            return jRadioAdmin.getText().toUpperCase();
        }
        else if(jRadioReceptionist.isSelected()){
            return jRadioReceptionist.getText().toLowerCase();
        }
        else if(jRadioDoctor.isSelected()){
            return jRadioDoctor.getText().toLowerCase();
        }
        else
            return null;
    }

