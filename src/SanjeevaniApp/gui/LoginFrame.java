    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
boolean isValidInput=validateInput();
if(isValidInput==false){
    JOptionPane.showMessageDialog(null,"UserId or Password cannot be left empty! ","Error!",JOptionPane.ERROR_MESSAGE);
    return;
}
String userType=getUserType();
if(userType==null){
  JOptionPane.showMessageDialog(null,"Please choose user type! ","Error!",JOptionPane.ERROR_MESSAGE);  

return;


}
    
 try
    {
      UserPojo user=new UserPojo();
      user.setUserId(userId);
      user.setPassword(password);
      user.setUserType(userType);
      String username=UserDao.validateUser(user);
      if(username!=null){
          JOptionPane.showMessageDialog(null,"Login Accepted! ","Success!",JOptionPane.INFORMATION_MESSAGE);
          UserProfile.setUsertype(userType);
          UserProfile.setUsername(username);
          
      
      if(userType.equalsIgnoreCase("admin")){
          AdminOptionsFrame adminOptionsFrame= new  AdminOptionsFrame();
           adminOptionsFrame.setVisible(true);
      }
     else if(userType.equalsIgnoreCase("receptionist")){
          ReceptionistOptionsFrame receptionistOptionsFrame= new  ReceptionistOptionsFrame();
           receptionistOptionsFrame.setVisible(true);
     }
     else if(userType.equalsIgnoreCase("doctor")){
           String uid=txtUser.getText();
           UserProfile.setUid(uid);
          DoctorsOptionFrame doctorOptionsFrame= new  DoctorsOptionFrame();
           doctorOptionsFrame.setVisible(true);
      }
    
      this.dispose();
    }
      
    
    else{
            JOptionPane.showMessageDialog(null,"Invalid user id or password ! ","Error!",JOptionPane.ERROR_MESSAGE);  
              }
    
    }
 
    catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,"Login DB Error ! "+ex,"Exception!",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
        }
    
    }//GEN-LAST:event_btnLoginActionPerformed

