    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
 try
        {
            EmpPojo p = new EmpPojo();
            
            p.setEmpid(empid);
            String name = txtEmpname.getText();
            p.setEmpname(name);
            String sal = txtSal.getText();
            p.setSal(Double.parseDouble(sal));
            String emprole=txtRole.getText();
            p.setEmprole(emprole);
            System.out.println("..........");
            boolean a =EmpDao.updateEmp(p);
            System.out.println(a);
           
            if(a==true)
            {
                JOptionPane.showMessageDialog(null,"Employee Successfully updated!", name,JOptionPane.INFORMATION_MESSAGE);
               
            }
            
        }
        catch(SQLException e)
        {
            System.out.println("Some Exception Occur in DB");  
        }
        catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null,"Please input Valid data Like Empid=101 ,name='abc',salry=123");
        }
         
 
        
    }//GEN-LAST:event_btnUpdateActionPerformed

