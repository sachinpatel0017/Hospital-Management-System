    private void jcEmpIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcEmpIdActionPerformed
      if(jcEmpId.getSelectedItem()==null)
            return;
        empid = jcEmpId.getSelectedItem().toString();
        try
        {
            System.out.println("before pojo");
        EmpPojo e = EmpDao.findEmpById(empid);
            System.out.println("after pojo");
        //jcEmpId.setSelectedItem(e.getEmpid());
        txtEmpname.setText(e.getEmpname());
        txtSal.setText(String.valueOf(e.getSal()));
        txtRole.setText(e.getEmprole());

        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,"Some Error in DB","Error",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jcEmpIdActionPerformed

