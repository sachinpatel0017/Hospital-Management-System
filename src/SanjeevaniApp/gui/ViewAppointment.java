      private void showAppData(){
        try{
            uid=UserProfile.getUid();
            String did=DoctorDao.getDid(uid);
            ArrayList<PatientPojo> al=DoctorDao.getDAPP(did);
            Object rows[]=new Object[3];
            DefaultTableModel dtm=(DefaultTableModel)abc.getModel();
            for(PatientPojo e:al){
                rows[0]=e.getP_id();
                rows[1]=e.getP_name();
                rows[2]=e.getOpd();
               
                dtm.addRow(rows);
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DBEroor:"+ex,"Exception",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

    
